final _magicCode = Uint8List.fromList([149, 39, 95, 27, 149, 39, 95, 27]);

Future<void> encodeFile(String src, String dest) async {
  var s = File(src);
  var d = File(dest);
  if (await d.exists()) {
    await d.delete();
  }

  var sbuf = await s.readAsBytes();
  var dbuf = Uint8List.fromList(sbuf.map((v) => _encodeTable[v]).toList());

  var oBuf = _magicCode + dbuf;

  var decodedBuf = decodeResource(Uint8List.fromList(oBuf));

  assert(decodedBuf.length == sbuf.length);
  for (int i=0; i<sbuf.length; i++ ) {
    assert(decodedBuf[i] == sbuf[i], '$i');
  }

  await d.writeAsBytes(oBuf, mode: FileMode.write, flush: true);
}


Uint8List decodeResource(Uint8List src) {
  if (src.length < _magicCode.length) return src;

  for (int i = 0; i < _magicCode.length; i++) {
    if (src[i] != _magicCode[i]) return src;
  }

  return Uint8List.fromList(
    List<int>.generate(
      src.length - _magicCode.length,
      (i) => _decodeTable[src[i + _magicCode.length]],
    ),
  );
}


final excludePaths = [
  'icon/',
  'fonts/',
  'region/',
];

void main() async {
  var bd = Directory.fromUri(Uri.directory('./resources'));
  await bd.list(recursive: true, followLinks: false).forEach((fse) async {
    if (fse is! File) return;
    var f = fse as File;
    if (f.path.startsWith('.')) return;
    if (f.path.length < 10) return;

    var tmp = f.path.substring(10);
    if (excludePaths.any((s) => tmp.startsWith(s))) return;

    var d = File('assets/' + tmp);
    await encodeFile(f.path, d.path);

    print('$tmp => ${d.path} => ${d.existsSync()}');
  });
}
